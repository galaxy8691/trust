fn emit_fn(
    out: &mut String,
    f: &IRFunction,
    level: usize,
    opts: &CodegenOptions,
    module: &IRModule,
) -> Result<(), CompileError> {
    if !f.type_params.is_empty() {
        return Err(diag(
            f.cm.as_ref(),
            &f.source_path,
            f.span,
            format!(
                "internal error: generic function `{}` must be monomorphized before codegen",
                f.name
            ),
        ));
    }
    let ind = indent(level);
    emit_frozen_ts_leading_comments(out, &ind, module, &f.source_path, f.span, opts);
    let name = rust_fn_name(&f.name);
    out.push_str(&ind);
    if f.is_async {
        out.push_str("async ");
    }
    out.push_str("fn ");
    out.push_str(name);
    out.push('(');
    for (i, (p, ty)) in f.params.iter().enumerate() {
        if i > 0 {
            out.push_str(", ");
        }
        out.push_str(p);
        out.push_str(": ");
        out.push_str(&rust_ty(ty, f)?);
    }
    out.push_str(") -> ");
    out.push_str(&rust_ty(&f.ret, f)?);
    out.push_str(" {\n");
    for s in &f.body {
        emit_stmt(out, s, level + 1, f, opts, module)?;
    }
    out.push_str(&ind);
    out.push_str("}\n");
    if level == 0 {
        out.push('\n');
    }
    Ok(())
}

