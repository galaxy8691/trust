fn emit_json_object_field_get(o: &str, prop: &str, kind: ObjectMemberAccessKind) -> String {
    match kind {
        ObjectMemberAccessKind::NumberLeaf => format!(
            "(({o}).get({prop:?}).expect(\"missing field\").as_f64().expect(\"expected JSON number field\"))"
        ),
        ObjectMemberAccessKind::NestedObject => format!(
            "(({o}).get({prop:?}).expect(\"missing field\").clone())"
        ),
        // R2: Method field - return the method name string wrapped in a special object
        ObjectMemberAccessKind::Method => format!(
            "serde_json::json!({{\"__trust_method\": ({o}).get({prop:?}).expect(\"missing method field\").as_str().expect(\"method field should be string\")}})"
        ),
    }
}

fn emit_object_lit_json(
    fields: &[(String, IRExpr)],
    f: &IRFunction,
    stmt_level: usize,
    module: &IRModule,
) -> Result<String, CompileError> {
    if fields.is_empty() {
        return Ok("serde_json::json!({})".to_string());
    }
    let mut parts = Vec::with_capacity(fields.len());
    for (k, v) in fields {
        let k_json = serde_json::to_string(k).map_err(|e| {
            diag(
                f.cm.as_ref(),
                &f.source_path,
                f.span,
                format!("object key JSON encoding: {e}"),
            )
        })?;
        let ve = emit_expr(v, f, stmt_level, module)?;
        parts.push(format!("{k_json}: ({ve})"));
    }
    Ok(format!("serde_json::json!({{{}}})", parts.join(", ")))
}

/// R2: Check if any field is a function reference (for deep chains with methods)
fn has_function_typed_field(fields: &[(String, IRExpr)]) -> bool {
    fields.iter().any(|(_, v)| matches!(v, IRExpr::Ident(..)))
}

/// R2: Emit object literal with function fields
/// For objects with methods, store method names as strings for runtime dispatch
fn emit_object_lit_with_methods(
    fields: &[(String, IRExpr)],
    f: &IRFunction,
    stmt_level: usize,
    module: &IRModule,
) -> Result<String, CompileError> {
    let mut parts = Vec::with_capacity(fields.len());
    for (k, v) in fields {
        match v {
            IRExpr::Ident(fn_name, _) => {
                // Store method name as string for runtime dispatch
                parts.push(format!("{k:?}: {fn_name:?}"));
            }
            _ => {
                let ve = emit_expr(v, f, stmt_level, module)?;
                parts.push(format!("{k:?}: ({ve})"));
            }
        }
    }
    Ok(format!("serde_json::json!({{{}}})", parts.join(", ")))
}

/// 代码生成选项中的标准库模式（CLI 兼容保留；**实现均在 `trust_stdlib` crate**，输出与模式无关）。
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum StdlibMode {
    /// 历史别名；生成代码与 `TrustStdlib` 相同。
    Legacy,
    #[default]
    TrustStdlib,
}

#[derive(Debug, Clone, Default)]
pub struct CodegenOptions {
    /// 在每条语句前写入 `// ts: path:line:col`（与诊断格式一致）。
    pub span_comments: bool,
    /// 在语句（及顶层函数）前写入 TS 源码中的 leading 注释（`//` / `/* */` 转为 Rust 行注释）。
    pub emit_ts_source_comments: bool,
    /// 标准库模式（保留 CLI；运行时实现统一在 `trust_stdlib`）。
    pub stdlib_mode: StdlibMode,
}

pub fn emit_rust(module: &IRModule) -> Result<String, CompileError> {
    emit_rust_with_options(module, &CodegenOptions::default())
}

pub fn emit_rust_with_options(
    module: &IRModule,
    opts: &CodegenOptions,
) -> Result<String, CompileError> {
    let _ = opts.stdlib_mode;
    let mut out = String::new();
    out.push_str("// Generated by trust — do not edit by hand.\n\n");

    // R2: Helper function for calling methods on JSON objects with method names stored as strings
    out.push_str("fn __trust_call_method(obj: &serde_json::Value, method: &str) -> serde_json::Value {\n");
    out.push_str("    serde_json::json!({\"__trust_method\": method, \"__trust_receiver\": obj.clone()})\n");
    out.push_str("}\n\n");

    if module_needs_http_stream(module) {
        out.push_str("use futures_util::StreamExt;\n\n#[derive(Clone)]\nstruct __TrustStreamReadResult {\n    done: bool,\n    value: Vec<u8>,\n}\n\n");
    }

    for c in &module.classes {
        let trait_name = format!("{}Dyn", c.name);
        let struct_name = format!("{}Obj", c.name);
        out.push_str(&format!("trait {trait_name} {{\n"));
        out.push_str("    fn __class_name(&self) -> &'static str;\n");
        out.push_str("}\n");
        out.push_str(&format!(
            "struct {struct_name} {{ data: std::collections::HashMap<String, f64> }}\n"
        ));
        out.push_str(&format!("impl {trait_name} for {struct_name} {{\n"));
        out.push_str("    fn __class_name(&self) -> &'static str {\n");
        // `Debug` escaping prevents `"` / `\n` in TS class names from breaking the generated literal.
        out.push_str(&format!("        {:?}\n", c.name));
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    let main_fn = module
        .fns
        .iter()
        .find(|f| f.name == "main")
        .expect("sem ensures main exists");

    for f in &module.fns {
        emit_fn(&mut out, f, 0, opts, module)?;
    }

    if main_fn.is_async {
        out.push_str("#[tokio::main]\nasync fn main() {\n");
    } else {
        out.push_str("fn main() {\n");
    }
    let ret_ty_s = rust_ty(&main_fn.ret, main_fn)?;
    let ret_ty = ret_ty_s.as_str();
    let await_main = if main_fn.is_async { ".await" } else { "" };
    match ret_ty {
        "()" => {
            out.push_str(&format!("    ts_main(){await_main};\n"));
        }
        "f64" | "bool" | "String" => {
            out.push_str(&format!("    println!(\"{{}}\", ts_main(){await_main});\n"));
        }
        "Vec<f64>"
        | "Vec<String>"
        | "Vec<reqwest::Response>"
        | "std::collections::HashMap<String, f64>"
        | "serde_json::Value" => {
            out.push_str(&format!(
                "    println!(\"{{:?}}\", ts_main(){await_main});\n"
            ));
        }
        _ => {
            out.push_str(&format!(
                "    println!(\"{{:?}}\", ts_main(){await_main});\n"
            ));
        }
    }
    out.push_str("}\n");

    Ok(out)
}

fn module_needs_http_stream(module: &IRModule) -> bool {
    module.fns.iter().any(|f| stmts_maybe_http_stream(&f.body))
}

fn stmts_maybe_http_stream(stmts: &[IRStmt]) -> bool {
    stmts.iter().any(|s| match s {
        IRStmt::Let { init, .. } => init.as_ref().is_some_and(expr_maybe_http_stream),
        IRStmt::Assign { rhs, .. } => expr_maybe_http_stream(rhs),
        IRStmt::MemberAssign { rhs, .. } => expr_maybe_http_stream(rhs),
        IRStmt::Expr { expr, .. } => expr_maybe_http_stream(expr),
        IRStmt::Return { arg: Some(e), .. } => expr_maybe_http_stream(e),
        IRStmt::Return { arg: None, .. } => false,
        IRStmt::Block { stmts: b, .. } => stmts_maybe_http_stream(b),
        IRStmt::If {
            cond,
            then_b,
            else_b,
            ..
        } => {
            expr_maybe_http_stream(cond)
                || stmts_maybe_http_stream(then_b)
                || else_b.as_deref().is_some_and(stmts_maybe_http_stream)
        }
        IRStmt::While { cond, body, .. } => {
            expr_maybe_http_stream(cond) || stmts_maybe_http_stream(body)
        }
        IRStmt::ForIn { target, body, .. } => {
            expr_maybe_http_stream(target) || stmts_maybe_http_stream(body)
        }
        IRStmt::DoWhile { body, cond, .. } => {
            stmts_maybe_http_stream(body) || expr_maybe_http_stream(cond)
        }
        IRStmt::FnDecl { func, .. } => stmts_maybe_http_stream(&func.body),
        IRStmt::Empty { .. } | IRStmt::Break { .. } | IRStmt::Continue { .. } => false,
    })
}

fn expr_maybe_http_stream(e: &IRExpr) -> bool {
    match e {
        IRExpr::HttpResponseBodyGetReader { .. } | IRExpr::ReaderRead { .. } => true,
        IRExpr::Await { arg, .. } => expr_maybe_http_stream(arg),
        IRExpr::Binary { left, right, .. } => {
            expr_maybe_http_stream(left) || expr_maybe_http_stream(right)
        }
        IRExpr::Unary { arg, .. } => expr_maybe_http_stream(arg),
        IRExpr::Call { args, .. }
        | IRExpr::OptionalCall { args, .. }
        | IRExpr::BuiltinLog { args, .. }
        | IRExpr::MathBuiltin { args, .. }
        | IRExpr::NumberBuiltin { args, .. }
        | IRExpr::JsonBuiltin { args, .. }
        | IRExpr::UriBuiltin { args, .. } => args.iter().any(expr_maybe_http_stream),
        IRExpr::MethodCall { receiver, args, .. }
        | IRExpr::OptionalMethodCall { receiver, args, .. } => {
            expr_maybe_http_stream(receiver) || args.iter().any(expr_maybe_http_stream)
        }
        IRExpr::StringMethodBuiltin { receiver, args, .. } => {
            expr_maybe_http_stream(receiver) || args.iter().any(expr_maybe_http_stream)
        }
        IRExpr::Conditional {
            test, cons, alt, ..
        } => {
            expr_maybe_http_stream(test)
                || expr_maybe_http_stream(cons)
                || expr_maybe_http_stream(alt)
        }
        IRExpr::Seq { exprs, .. } => exprs.iter().any(expr_maybe_http_stream),
        IRExpr::Tpl { parts, .. } => parts.iter().any(|p| {
            if let TplPart::Interp(x) = p {
                expr_maybe_http_stream(x)
            } else {
                false
            }
        }),
        IRExpr::Member { obj, .. } | IRExpr::OptionalMember { obj, .. } => {
            expr_maybe_http_stream(obj)
        }
        IRExpr::NullishCoalesce { left, right, .. } => {
            expr_maybe_http_stream(left) || expr_maybe_http_stream(right)
        }
        IRExpr::ArrayLit { elems, .. } => elems.iter().any(expr_maybe_http_stream),
        IRExpr::ObjectLit { fields, .. } => fields.iter().any(|(_, v)| expr_maybe_http_stream(v)),
        IRExpr::Index { obj, index, .. } => {
            expr_maybe_http_stream(obj) || expr_maybe_http_stream(index)
        }
        IRExpr::ArrowFn { body, .. } => stmts_maybe_http_stream(body),
        IRExpr::PromiseAll { elems, .. } => elems.iter().any(expr_maybe_http_stream),
        IRExpr::ReadFileText { path, .. } | IRExpr::ReadFileTextAsync { path, .. } => {
            expr_maybe_http_stream(path)
        }
        IRExpr::Fetch { .. }
        | IRExpr::HttpResponseMethodBuiltin { .. }
        | IRExpr::Number(..)
        | IRExpr::Bool(..)
        | IRExpr::Str(..)
        | IRExpr::Ident(..)
        | IRExpr::Null(..)
        | IRExpr::Undefined(..)
        | IRExpr::This(..)
        | IRExpr::Super(..)
        | IRExpr::ReadStdinLine { .. }
        | IRExpr::FetchText { .. }
        | IRExpr::RustNew { .. } => false,
    }
}

