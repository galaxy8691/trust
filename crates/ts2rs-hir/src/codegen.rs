//! HIR → Rust 源码。

use crate::error::{diag, CompileError};
use crate::ir::*;
use swc_common::Span;

pub fn emit_rust(module: &IRModule) -> Result<String, CompileError> {
    let mut out = String::new();
    out.push_str("// Generated by ts2rs — do not edit by hand.\n\n");

    let main_fn = module
        .fns
        .iter()
        .find(|f| f.name == "main")
        .expect("sem ensures main exists");

    for f in &module.fns {
        emit_fn(&mut out, f, 0)?;
    }

    out.push_str("fn main() {\n");
    let ret_ty = rust_ty(&main_fn.ret, main_fn)?;
    match ret_ty {
        "()" => {
            out.push_str("    ts_main();\n");
        }
        "i32" | "bool" | "String" => {
            out.push_str("    println!(\"{}\", ts_main());\n");
        }
        "Vec<i32>" | "std::collections::HashMap<String, i32>" => {
            out.push_str("    println!(\"{:?}\", ts_main());\n");
        }
        _ => {
            return Err(diag(
                main_fn.cm.as_ref(),
                &main_fn.source_path,
                main_fn.span,
                "unexpected return type classification in codegen",
            ));
        }
    }
    out.push_str("}\n");

    Ok(out)
}

fn rust_fn_name(name: &str) -> &str {
    if name == "main" {
        "ts_main"
    } else {
        name
    }
}

fn is_numberish(t: &TsType) -> bool {
    match t {
        TsType::Number | TsType::NumberLit(_) => true,
        TsType::Union(m) => m.iter().all(is_numberish),
        _ => false,
    }
}

fn rust_ty_scalar(t: &TsType) -> &'static str {
    match t {
        TsType::Number | TsType::NumberLit(_) => "i32",
        TsType::Boolean | TsType::BoolLit(_) => "bool",
        TsType::String | TsType::StringLit(_) => "String",
        TsType::Void => "()",
        TsType::Null => "()",
        TsType::Undefined => "()",
        TsType::ArrayNumber => "Vec<i32>",
        TsType::ObjectNum(_) => "std::collections::HashMap<String, i32>",
        TsType::Union(_) => unreachable!("rust_ty_scalar: use rust_ty for unions"),
    }
}

fn rust_ty(t: &TsType, f: &IRFunction) -> Result<&'static str, CompileError> {
    match t {
        TsType::Union(members) => {
            if members.is_empty() {
                return Err(diag(
                    f.cm.as_ref(),
                    &f.source_path,
                    f.span,
                    "empty union type",
                ));
            }
            let mut it = members.iter();
            let first = rust_ty(it.next().unwrap(), f)?;
            for m in it {
                let r = rust_ty(m, f)?;
                if r != first {
                    return Err(diag(
                        f.cm.as_ref(),
                        &f.source_path,
                        f.span,
                        "union type cannot be mapped to a single Rust type (heterogeneous members)",
                    ));
                }
            }
            Ok(first)
        }
        _ => Ok(rust_ty_scalar(t)),
    }
}

fn emit_fn(out: &mut String, f: &IRFunction, level: usize) -> Result<(), CompileError> {
    let ind = indent(level);
    let name = rust_fn_name(&f.name);
    out.push_str(&ind);
    out.push_str("fn ");
    out.push_str(name);
    out.push('(');
    for (i, (p, ty)) in f.params.iter().enumerate() {
        if i > 0 {
            out.push_str(", ");
        }
        out.push_str(p);
        out.push_str(": ");
        out.push_str(rust_ty(ty, f)?);
    }
    out.push_str(") -> ");
    out.push_str(rust_ty(&f.ret, f)?);
    out.push_str(" {\n");
    for s in &f.body {
        emit_stmt(out, s, level + 1, f)?;
    }
    out.push_str(&ind);
    out.push_str("}\n");
    if level == 0 {
        out.push('\n');
    }
    Ok(())
}

fn indent(n: usize) -> String {
    "    ".repeat(n)
}

fn emit_stmt(
    out: &mut String,
    s: &IRStmt,
    level: usize,
    f: &IRFunction,
) -> Result<(), CompileError> {
    let ind = indent(level);
    match s {
        IRStmt::Empty { .. } => {}
        IRStmt::Let {
            name,
            ty,
            init,
            mutable,
            ..
        } => {
            out.push_str(&ind);
            if *mutable {
                out.push_str("let mut ");
            } else {
                out.push_str("let ");
            }
            out.push_str(name);
            out.push_str(": ");
            out.push_str(rust_ty(ty, f)?);
            out.push_str(" = ");
            out.push_str(&emit_expr(init, f)?);
            out.push_str(";\n");
        }
        IRStmt::Assign { name, rhs, .. } => {
            out.push_str(&ind);
            out.push_str(name);
            out.push_str(" = ");
            out.push_str(&emit_expr(rhs, f)?);
            out.push_str(";\n");
        }
        IRStmt::Expr { expr, .. } => {
            out.push_str(&ind);
            out.push_str(&emit_expr_stmt(expr, f)?);
            out.push_str(";\n");
        }
        IRStmt::Return { arg, .. } => {
            out.push_str(&ind);
            out.push_str("return ");
            if let Some(e) = arg {
                out.push_str(&emit_expr(e, f)?);
            }
            out.push_str(";\n");
        }
        IRStmt::Block { stmts, .. } => {
            out.push_str(&ind);
            out.push_str("{\n");
            for x in stmts {
                emit_stmt(out, x, level + 1, f)?;
            }
            out.push_str(&ind);
            out.push_str("}\n");
        }
        IRStmt::If {
            cond,
            cond_ty,
            then_b,
            else_b,
            ..
        } => {
            out.push_str(&ind);
            out.push_str("if ");
            let c = emit_expr(cond, f)?;
            if is_numberish(cond_ty) {
                out.push_str(&format!("({c}) != 0"));
            } else {
                out.push_str(&format!("({c})"));
            }
            out.push_str(" {\n");
            for x in then_b {
                emit_stmt(out, x, level + 1, f)?;
            }
            out.push_str(&ind);
            out.push('}');
            if let Some(else_b) = else_b {
                out.push_str(" else {\n");
                for x in else_b {
                    emit_stmt(out, x, level + 1, f)?;
                }
                out.push_str(&ind);
                out.push_str("}\n");
            } else {
                out.push('\n');
            }
        }
        IRStmt::While {
            cond,
            cond_ty,
            body,
            ..
        } => {
            out.push_str(&ind);
            out.push_str("while ");
            let c = emit_expr(cond, f)?;
            if is_numberish(cond_ty) {
                out.push_str(&format!("({c}) != 0"));
            } else {
                out.push_str(&format!("({c})"));
            }
            out.push_str(" {\n");
            for x in body {
                emit_stmt(out, x, level + 1, f)?;
            }
            out.push_str(&ind);
            out.push_str("}\n");
        }
        IRStmt::DoWhile {
            body,
            cond,
            cond_ty,
            ..
        } => {
            out.push_str(&ind);
            out.push_str("loop {\n");
            for x in body {
                emit_stmt(out, x, level + 1, f)?;
            }
            let c = emit_expr(cond, f)?;
            out.push_str(&indent(level + 1));
            if is_numberish(cond_ty) {
                out.push_str(&format!("if ({c}) == 0 {{ break; }}\n"));
            } else {
                out.push_str(&format!("if !({c}) {{ break; }}\n"));
            }
            out.push_str(&ind);
            out.push_str("}\n");
        }
        IRStmt::Break { .. } => {
            out.push_str(&ind);
            out.push_str("break;\n");
        }
        IRStmt::Continue { .. } => {
            out.push_str(&ind);
            out.push_str("continue;\n");
        }
        IRStmt::FnDecl { func, .. } => {
            emit_fn(out, func, level)?;
        }
    }
    Ok(())
}

fn emit_expr_stmt(e: &IRExpr, f: &IRFunction) -> Result<String, CompileError> {
    match e {
        IRExpr::BuiltinLog { args, .. } => emit_builtin_log(args, f),
        _ => emit_expr(e, f),
    }
}

fn emit_builtin_log(args: &[IRExpr], f: &IRFunction) -> Result<String, CompileError> {
    if args.is_empty() {
        return Ok("println!()".to_string());
    }
    let mut parts = Vec::new();
    for a in args {
        parts.push(emit_expr(a, f)?);
    }
    if parts.len() == 1 {
        return Ok(format!("println!(\"{{}}\", {})", parts[0]));
    }
    let mut fmt_str = String::new();
    for _ in 0..parts.len() {
        fmt_str.push_str("{}");
    }
    let joined = parts.join(", ");
    Ok(format!("println!(\"{fmt_str}\", {joined})"))
}

fn emit_expr(e: &IRExpr, f: &IRFunction) -> Result<String, CompileError> {
    match e {
        IRExpr::Number(n, _) => Ok(format!("{n}")),
        IRExpr::Bool(b, _) => Ok(format!("{b}")),
        IRExpr::Str(s, _) => Ok(format!("{:?}.to_string()", s)),
        IRExpr::Null(_) | IRExpr::Undefined(_) => Ok("()".to_string()),
        IRExpr::Ident(name, _) => Ok(name.clone()),
        IRExpr::Unary { op, arg, .. } => {
            let a = emit_expr(arg, f)?;
            match op {
                IRUnaryOp::Neg => Ok(format!("(-({a}))")),
                IRUnaryOp::Not => Ok(format!("!({a})")),
            }
        }
        IRExpr::Binary {
            op,
            left,
            right,
            kind,
            span,
        } => {
            let l = emit_expr(left, f)?;
            let r = emit_expr(right, f)?;
            use IRBinOp::*;
            let mid = match op {
                Add => {
                    if matches!(kind, Some(BinaryKind::StrConcat)) {
                        format!("format!(\"{{}}{{}}\", {l}, {r})")
                    } else {
                        format!("({l} + {r})")
                    }
                }
                Sub => format!("({l} - {r})"),
                Mul => format!("({l} * {r})"),
                Div => format!("({l} / {r})"),
                Eq => format!("({l} == {r})"),
                Ne => format!("({l} != {r})"),
                Lt => format!("({l} < {r})"),
                Le => format!("({l} <= {r})"),
                Gt => format!("({l} > {r})"),
                Ge => format!("({l} >= {r})"),
                LogicalAnd | LogicalOr => {
                    let (lhs_n, rhs_n) = match kind {
                        Some(BinaryKind::Logical {
                            lhs_number_truthy,
                            rhs_number_truthy,
                        }) => (*lhs_number_truthy, *rhs_number_truthy),
                        _ => {
                            return Err(diag(
                                f.cm.as_ref(),
                                &f.source_path,
                                *span,
                                "`&&` / `||` missing Logical kind (semantic pass bug)",
                            ));
                        }
                    };
                    let ls = if lhs_n {
                        format!("({l} != 0)")
                    } else {
                        l
                    };
                    let rs = if rhs_n {
                        format!("({r} != 0)")
                    } else {
                        r
                    };
                    match op {
                        LogicalAnd => format!("({ls} && {rs})"),
                        LogicalOr => format!("({ls} || {rs})"),
                        _ => unreachable!(),
                    }
                }
            };
            Ok(mid)
        }
        IRExpr::Call { callee, args, .. } => {
            let name = rust_fn_name(callee);
            let mut a = Vec::new();
            for x in args {
                a.push(emit_expr(x, f)?);
            }
            Ok(format!("{}({})", name, a.join(", ")))
        }
        IRExpr::BuiltinLog { span, .. } => Err(diag(
            f.cm.as_ref(),
            &f.source_path,
            *span,
            "console.log must be in expression statement position",
        )),
        IRExpr::Conditional {
            test,
            cons,
            alt,
            cond_ty,
            span,
            ..
        } => {
            let ct = cond_ty.as_ref().ok_or_else(|| {
                diag(
                    f.cm.as_ref(),
                    &f.source_path,
                    *span,
                    "ternary condition type not filled by sem",
                )
            })?;
            let c = emit_expr(test, f)?;
            let cond_s = if is_numberish(ct) {
                format!("({c}) != 0")
            } else {
                format!("({c})")
            };
            let t = emit_expr(cons, f)?;
            let a = emit_expr(alt, f)?;
            Ok(format!("(if {cond_s} {{ {t} }} else {{ {a} }})"))
        }
        IRExpr::Seq { exprs, span } => emit_seq_expr(exprs, *span, f),
        IRExpr::Tpl { parts, .. } => emit_tpl(parts, f),
        IRExpr::Member { obj, prop, .. } => {
            if prop == "length" {
                let o = emit_expr(obj, f)?;
                Ok(format!("({o}.len() as i32)"))
            } else {
                let o = emit_expr(obj, f)?;
                Ok(format!("(*{o}.get({:?}).expect(\"missing field\"))", prop))
            }
        }
        IRExpr::OptionalMember { obj, prop, .. } => match &**obj {
            IRExpr::Null(_) | IRExpr::Undefined(_) => Ok("()".to_string()),
            _ => {
                if prop == "length" {
                    let o = emit_expr(obj, f)?;
                    Ok(format!("({o}.len() as i32)"))
                } else {
                    let o = emit_expr(obj, f)?;
                    Ok(format!("(*{o}.get({:?}).expect(\"missing field\"))", prop))
                }
            }
        },
        IRExpr::NullishCoalesce { left, right, .. } => {
            if matches!(&**left, IRExpr::Null(_) | IRExpr::Undefined(_)) {
                emit_expr(right, f)
            } else {
                emit_expr(left, f)
            }
        }
        IRExpr::ArrayLit { elems, .. } => {
            let mut xs = Vec::with_capacity(elems.len());
            for e in elems {
                xs.push(emit_expr(e, f)?);
            }
            Ok(format!("vec![{}]", xs.join(", ")))
        }
        IRExpr::ObjectLit { fields, .. } => {
            if fields.is_empty() {
                return Ok("std::collections::HashMap::new()".to_string());
            }
            let mut pairs = Vec::with_capacity(fields.len());
            for (k, v) in fields {
                pairs.push(format!("({:?}.to_string(), {})", k, emit_expr(v, f)?));
            }
            Ok(format!("std::collections::HashMap::from([{}])", pairs.join(", ")))
        }
        IRExpr::Index { obj, index, .. } => {
            let o = emit_expr(obj, f)?;
            let i = emit_expr(index, f)?;
            Ok(format!("({o}[({i}) as usize])"))
        }
    }
}

fn emit_seq_expr(
    exprs: &[IRExpr],
    span: Span,
    f: &IRFunction,
) -> Result<String, CompileError> {
    if exprs.is_empty() {
        return Err(diag(
            f.cm.as_ref(),
            &f.source_path,
            span,
            "empty sequence",
        ));
    }
    if exprs.len() == 1 {
        return emit_expr(&exprs[0], f);
    }
    let mut block = String::new();
    for e in &exprs[..exprs.len() - 1] {
        block.push_str(&emit_expr_stmt(e, f)?);
        block.push_str(";\n");
    }
    block.push_str(&emit_expr(&exprs[exprs.len() - 1], f)?);
    Ok(format!("({{\n{block}\n}})"))
}

fn emit_tpl(parts: &[TplPart], f: &IRFunction) -> Result<String, CompileError> {
    if parts.is_empty() {
        return Ok("\"\".to_string()".to_string());
    }
    if parts.len() == 1 {
        if let TplPart::Static(s) = &parts[0] {
            return Ok(format!("{:?}.to_string()", s));
        }
    }
    let mut fmt = String::new();
    let mut args: Vec<String> = Vec::new();
    for p in parts {
        match p {
            TplPart::Static(s) => {
                for ch in s.chars() {
                    match ch {
                        '{' => fmt.push_str("{{"),
                        '}' => fmt.push_str("}}"),
                        _ => fmt.push(ch),
                    }
                }
            }
            TplPart::Interp(e) => {
                fmt.push_str("{}");
                args.push(emit_expr(e, f)?);
            }
        }
    }
    let fmt_esc = fmt.replace('\\', "\\\\").replace('"', "\\\"");
    if args.is_empty() {
        return Ok(format!("format!(\"{fmt_esc}\")"));
    }
    Ok(format!(
        "format!(\"{fmt_esc}\", {})",
        args.join(", ")
    ))
}
